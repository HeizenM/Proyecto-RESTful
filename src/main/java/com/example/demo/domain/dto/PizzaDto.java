package com.example.demo.domain.dto;

public class PizzaDto {

    private Integer idPizzaDto;
    
    private String nameDto;
    
    private String descriptionDto;
    
    private Double priceDto;
    
    private Boolean vegetarianDto;
    
    private Boolean veganDto;
    
    private Boolean avaliableDto;

    public Integer getIdPizzaDto() {
        return idPizzaDto;
    }

    public void setIdPizzaDto(Integer idPizzaDto) {
        this.idPizzaDto = idPizzaDto;
    }

    public String getNameDto() {
        return nameDto;
    }

    public void setNameDto(String nameDto) {
        this.nameDto = nameDto;
    }

    public String getDescriptionDto() {
        return descriptionDto;
    }

    public void setDescriptionDto(String descriptionDto) {
        this.descriptionDto = descriptionDto;
    }

    public Double getPriceDto() {
        return priceDto;
    }

    public void setPriceDto(Double priceDto) {
        this.priceDto = priceDto;
    }

    public Boolean getVegetarianDto() {
        return vegetarianDto;
    }

    public void setVegetarianDto(Boolean vegetarianDto) {
        this.vegetarianDto = vegetarianDto;
    }

    public Boolean getVeganDto() {
        return veganDto;
    }

    public void setVeganDto(Boolean veganDto) {
        this.veganDto = veganDto;
    }

    public Boolean getAvaliableDto() {
        return avaliableDto;
    }

    public void setAvaliableDto(Boolean avaliableDto) {
        this.avaliableDto = avaliableDto;
    }

    

    
}
